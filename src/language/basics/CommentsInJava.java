package language.basics;

/**
 * This is the Project created to learn java concepts taught by bharath tech academy,
 * This class explains about commenting in a project such as
 * Singleline Comment
 * Multiline Comment
 * Documentation Comments.
 * 
 * @author HARISH R
 * @param No Parameters has been passed
 * @return No Output 
 */

public class CommentsInJava {
	
	public static void main(String[] args) {
		
//This print statement will print the content and will move to next line.(Single line comment)
		System.out.println("Hello world !");
		
		
/* This Print statement will print the content inside (),
 * 	and will be in same line
 * to comment multiple line we use Multiline Comment.	
 */ 
		System.out.print("My name is harish");
		System.out.println(",I am working in an ecommerce business website.");
		System.out.println();
	}

}
